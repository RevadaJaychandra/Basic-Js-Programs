let n1=5;
let n2=10;
let ch='+';
switch(ch){
    case '+':
        console.log(n1+n2);
        break;
    case '-':
        console.log(n1-n2);
        break;
    case '*':
        console.log(n1*n2);
        break;
    case '/':
        console.log(n1/n2);
        break;
    default:
        console.log("Invalid operator");
}